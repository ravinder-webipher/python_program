#predefined functions

#lower function
def class_subject1():
    sub1="ENGLISH"
    print("subject name is:", sub1)
    sub1=sub1.lower()
    print("subject name by applying lower function is: ",sub1)

#upper function
def class_subject2():
    sub2="maths"
    print()
    print("subject name is:", sub2)
    sub2=sub2.upper()
    print("subject name by applying upper function is: ",sub2)
    
#capitalize function
def class_subject3():
    sub3="punjabi"
    print()
    print("subject name is:", sub3)
    sub3=sub3.capitalize()
    print("subject name by applying capitalize function is: ",sub3)
    
#title function
def class_subject4():
    sub4="environment studies"
    print()
    print("subject name is:", sub4)
    sub4=sub4.title()
    print("subject name by applying title function is: ",sub4)
    
#swapcase function
def class_subject5():
    sub5="computer"
    print()
    print("subject name is:", sub5)
    sub5=sub5.swapcase()
    print("subject name by applying swap function is: ",sub5)
    sub5=sub5.swapcase()
    print("subject name by applying swap function again is: ",sub5)
    
    
class_subject1()
class_subject2()
class_subject3()
class_subject4()
class_subject5()


